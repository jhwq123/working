package study_230405;

public class Person {
	String name;
	int age;
	String sound;

	Person(String name) {
		this.name = name;
	}
	
	Person(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
	Person(String name, int age, String sound) {
		this.name = name;
		this.age = age;
		this.sound = sound;
	}
	
}


